// Dijkstra's algorithm for shortest path
function dijkstra(
  nodes: GraphNode[],
  edges: GraphEdge[],
  start: number,
  end: number,
  excludedEdges: Array<{ from: number; to: number }> = []
): number[] {
  const distances: { [key: number]: number } = {};
  const previous: { [key: number]: number | null } = {};
  const unvisited = new Set<number>();

  // Initialize distances
  nodes.forEach((node) => {
    distances[node.id] = node.id === start ? 0 : Number.POSITIVE_INFINITY;
    previous[node.id] = null;
    unvisited.add(node.id);
  });

  while (unvisited.size > 0) {
    // Find unvisited node with minimum distance
    const current = Array.from(unvisited).reduce((min, node) =>
      distances[node] < distances[min] ? node : min
    );

    if (distances[current] === Number.POSITIVE_INFINITY) break;
    if (current === end) break;

    unvisited.delete(current);

    // Check neighbors
    const availableEdges = edges.filter((edge) => {
      const isExcluded = excludedEdges.some(
        (ex) => ex.from === edge.from && ex.to === edge.to
      );

      return !isExcluded && edge.from === current;
    });

    availableEdges.forEach((edge) => {
      const neighbor = edge.to;
      if (!unvisited.has(neighbor)) return;
      if (neighbor >= 100 && neighbor !== end) return; // Skip special nodes unless it's the destination

      const alt = distances[current] + edge.distance;
      if (alt < distances[neighbor]) {
        distances[neighbor] = alt;
        previous[neighbor] = current;
      }
    });
  }

  // Reconstruct path
  const path: number[] = [];
  let current = end;

  while (current !== null) {
    path.unshift(current);
    current = previous[current];
  }

  return path[0] === start ? path : [];
}

export function getNodeTriplet(
  previous: number | null,
  current: number,
  next: number | null
): NodeTriplet {
  const triplet = NODE_TRIPLETS.find(
    (t) => t.previous === previous && t.current === current && t.next === next
  );

  if (!triplet) {
    throw new Error(
      `No triplet definition found for (${previous}, ${current}, ${next})`
    );
  }

  return triplet;
}

export function getEdgeInfo(from: number, to: number): GraphEdge | null {
  return (
    GRAPH_EDGES.find((edge) => edge.from === from && edge.to === to) || null
  );
}

export async function findRoute(
  departure: number,
  destination: number,
  excludedEdges: Array<{ from: number; to: number }> = [],
  rainyMode = false
) {
  // console.log(
  //   `Finding route from ${departure} to ${destination} (rainyMode: ${rainyMode})`
  // );
  if (destination === 169) {
    rainyMode = false;
  }
  const path = dijkstra(
    GRAPH_NODES,
    rainyMode ? GRAPH_EDGES_RAINY : GRAPH_EDGES,
    departure,
    destination,
    excludedEdges
  );

  if (path.length === 0) {
    throw new Error(`No route found from ${departure} to ${destination}`);
  }

  const route = [];

  // console.log("Path found:", path);

  for (let i = 0; i < path.length; i++) {
    const isStart = i === 0;
    const isEnd = i === path.length - 1;

    const previous = isStart ? path[i] : isEnd ? path[i - 2] : path[i - 1];
    const current = isStart ? path[i + 1] : isEnd ? path[i - 1] : path[i];
    const next = isStart ? path[i + 2] : isEnd ? path[i] : path[i + 1];

    try {
      const triplet = getNodeTriplet(previous, current, next);
      const edgeInfo = isEnd ? null : getEdgeInfo(current, next);

      const headline = isStart
        ? triplet.prev_name
        : isEnd
        ? triplet.next_name
        : triplet.curr_name;

      route.push({
        id: current.toString(),
        title: headline,
        image: triplet.image || "/placeholder.svg?height=400&width=400",
        // connector:
        //   edgeInfo?.notice?.text ||
        // (next ? `Continue to next location` : undefined),
        notice: edgeInfo?.notice,
      });
    } catch (error) {
      console.error(
        `Error getting triplet for (${previous}, ${current}, ${next}):`,
        error
      );
      throw error;

      // route.push({
      //   id: current.toString(),
      //   title: current.toString,
      //   description: "triplet.description",
      //   image: "placeholder.svg?height=400&width=400",
      //   connector: "Continue to next location",
      //   notice: null,
      // });
    }
  }

  const cost = path.slice(0, -1).reduce((sum, node, i) => {
    const edge = getEdgeInfo(node, path[i + 1]);
    return sum + (edge ? edge.distance : 0);
  }, 0);

  return {
    nodes: path,
    edges: path
      .slice(0, -1)
      .map((node, i) => ({ from: node, to: path[i + 1] })),
    route,
    cost,
  };
}
